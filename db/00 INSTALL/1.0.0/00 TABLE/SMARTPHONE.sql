CREATE TABLE `SMARTPHONE` (
    `ID` INT(11) NOT NULL AUTO_INCREMENT,
    `NAME` VARCHAR(255) NOT NULL,
    `BRAND` VARCHAR(255) NOT NULL,
    `RELEASED` VARCHAR(255) NOT NULL,
    `IMAGE_LINK` VARCHAR(255) NOT NULL,
    `DESIGN` INT(11) NOT NULL,
    `DISPLAY` DECIMAL(10 , 2 ) NOT NULL,
    `LENGTH` INT(1) NOT NULL,
    `WIDTH` INT(1) NOT NULL,
    `CPU` INT(1) NOT NULL,
    `UPDATES` INT(1) NOT NULL,
    `CAMERA` INT(1) NOT NULL,
    `BATTERY` INT(1) NOT NULL,
    `BATTERYSIZE` INT(1) NOT NULL,
    `SD_SLOT` INT(1) NOT NULL,
    `SIM_CARDS` INT(1) NOT NULL,
    `NOTCH` TINYINT(1) NOT NULL,
    `WATERPROOF` INT(1) NOT NULL,
    `HEADPHONE_JACK` INT(1) NOT NULL,
    PRIMARY KEY (`ID`)
)  ENGINE=INNODB DEFAULT CHARSET=LATIN1;